package com.company;


public class Point {


    {
    double x; // x coordinate
    double y; // y coordinate
    // two-argument constructor
             new Point(); double xCoordinate = 0; double yCoordinate = 0;
    {
        x = xCoordinate; // set x
        y = yCoordinate; // set y
    } // end two-argument Point constructor
    //return x
    double getX;
    {
        return x;
    } // end method getX

    // return y
    double getY;
    {
        return y;
    } // end method getY
    // return string representation of Point object
    String toString;
    {
        return String.format( "( %.1f, %.1f )", getX(),getY() );
    } // end method toString
} // end class Point



    }




